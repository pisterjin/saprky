/**
 * maskText — 사용자 입력 텍스트를 화면 표시용으로 마스킹
 *
 * 서버 전송 원문은 건드리지 않고, UI 렌더링 시에만 적용합니다.
 *
 * 적용 순서 (오탐 방지: 긴 패턴 먼저)
 *  1. 카드번호    (하이픈 있음) : 5966-5133-1234-8776  →  5966-5133-●●●●-8776
 *  2. 카드번호    (하이픈 없음) : 5966513312348776     →  5966513312348776 → 5966-5133-●●●●-8776 형태로
 *  3. 주민등록번호 (하이픈 있음) : 950427-1234567       →  950427-●●●●●●●
 *  4. 주민등록번호 (하이픈 없음) : 9504271234567        →  950427●●●●●●●
 *  5. 전화번호    (하이픈 있음) : 010-1234-5678         →  010-●●●●-5678
 *  6. 전화번호    (하이픈 없음) : 01012345678           →  010●●●●5678
 *  7. 주소 번지                : 고척로 25 / 고척동 3-2  →  고척로 ●●●●● / 고척동 ●●●●●
 */
export function maskText(text: string): string {
  return (
    text
      // 1) 카드번호 (하이픈 있음): 4-4-4-4 → 세 번째 그룹 마스킹
      .replace(/\b(\d{4})-(\d{4})-(\d{4})-(\d{4})\b/g, "$1-$2-●●●●-$4")

      // 2) 카드번호 (하이픈 없음): 정확히 16자리 연속 → 9~12번째 자리 마스킹
      .replace(/\b(\d{4})(\d{4})(\d{4})(\d{4})\b/g, "$1$2●●●●$4")

      // 3) 주민등록번호 (하이픈 있음): 6-7 → 뒷자리 전체 마스킹
      .replace(/\b(\d{6})-(\d{7})\b/g, "$1-●●●●●●●")

      // 4) 주민등록번호 (하이픈 없음): 정확히 13자리 연속
      .replace(/\b(\d{6})(\d{7})\b/g, "$1●●●●●●●")

      // 5) 전화번호 (하이픈 있음): 국번 2~4자리 - 중간 - 뒷번호
      .replace(/\b(\d{2,4})-(\d{3,4})-(\d{4})\b/g, "$1-●●●●-$3")

      // 6) 전화번호 (하이픈 없음): 0으로 시작하는 10~11자리 (02·010·031 등)
      .replace(/\b(0\d{1,2})(\d{3,4})(\d{4})\b/g, "$1●●●●$3")

      // 7) 주소 번지: 로·길·동·가·번길 뒤에 오는 숫자 마스킹
      //    예) 고척로 25  →  고척로 ●●●●●
      //        고척동 3-2 →  고척동 ●●●●●
      .replace(/([가-힣]+(로|길|동|가|번길|번지))\s+(\d[\d-]*)/g, "$1 ●●●●●")
  );
}
